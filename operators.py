# Operators in python

a = 5
b = 3

# Arithmetic operations
addition = a + b      # 8
diff = a - b          # 2
product = a * b       # 16
division = a / b      # 1.6
floor_division = a//b # 1 -> se aproxima al entero mas cercano
mod = a % b           # 2
exponential = a ** b  # 125 (a^b)

# + sirve para concatenar cadenas de texto
print("this is a diff: " + str(diff))

